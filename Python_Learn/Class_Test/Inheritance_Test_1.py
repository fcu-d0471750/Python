

#==============================================
#定義一個類別Animal
#==============================================
class Animal():
    # ==============================================
    # 屬性(Attribute)
    # ==============================================
    # 姓名
    Name = ''
    # 毛色
    Fur_color = ''

    # ==============================================
    # 建構子(Constructor)
    # ==============================================
    def __init__(self, Name, Fur_color):
        self.Name = Name
        self.Fur_color = Fur_color

    # ==============================================
    # 方法(Methods)
    # ==============================================
    # 吠叫
    def Bark(self):
        pass

#==============================================
#定義一個類別Dog
#==============================================
class Dog(Animal):

    #==============================================
    #建構子(Constructor)
    #==============================================
    def __init__(self , Name , Fur_color):
        self.Name = Name
        self.Fur_color = Fur_color

    #==============================================
    #方法(Methods)
    #==============================================
    #吠叫
    def Bark(self):
        print(self.Name , '叫了一聲')


#==============================================
#執行
#==============================================

#生成一個物件Dog
DD = Dog('DD' , 'White')

#DD叫了一聲
DD.Bark()











