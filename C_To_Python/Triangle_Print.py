'''
印出三角形

Height = 3
++*++
+***+
*****

Height = 5
++++*++++
+++***+++
++*****++
+*******+
*********

+的公式 = (最後一層的*數量 - 某一層的*數量)/2

'''

#=================================================================================
#宣告方法
#=================================================================================

#==========================
#印出(上半部)
#==========================
def Print_Triange_Upper(k):
    # 左邊空格
    for i in range(1, (((2 * Height - 1) - (2 * k - 1)) // 2) + 1, 1):
        print(' ', end='')
    # 中間
    for i in range(1, (2 * k), 1):
        print('*', end='')
    # 右邊空格
    for i in range(1, (((2 * Height - 1) - (2 * k - 1)) // 2) + 1, 1):
        print(' ', end='')
    print('')

#==========================
#印出(下半部)
#==========================
def Print_Triange_Lower(k):
    # 左邊空格
    for i in range(1, (((2 * Height - 1) - (2 * k - 1)) // 2) + 1, 1):
        print(' ', end='')
    # 中間
    for i in range(1, (2 * k), 1):
        print('*', end='')
    # 右邊空格
    for i in range(1, (((2 * Height - 1) - (2 * k - 1)) // 2) + 1, 1):
        print(' ', end='')
    print('')

#=================================================================================
#宣告變數
#=================================================================================

#Height : 三角形大小
Height = eval(input('三角形的高: '))

#=================================================================================
#執行(上半部)
#=================================================================================
for k in range(1,Height+1,1):
    Print_Triange_Upper(k)

#=================================================================================
#執行(下半部)
#=================================================================================
for k in range(Height-1,0,-1):
    Print_Triange_Lower(k)

for k in range(0,Height-1,1):
    print(k,end=' ')
print()
for k in range(Height-1,-1,-2):
    print(k,end=' ')
